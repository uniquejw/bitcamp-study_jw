package com.eomcs.lang.ex03;

public class Exam0210 {
  public static void main(String[] args) {
    System.out.println(100); // 10진수

    System.out.println(0144); //8진수

    System.out.println(0b1100100); //2진수 0 x
    System.out.println(0B1100100); //2진수 0 x

    System.out.println(0b01100100); //2진수 0 o
    System.out.println(0B01100100); //2진수 0 o

    System.out.println(0x64); //16진수 0 x
    System.out.println(0X64); //16진수 0 x

    System.out.println(0x064); //16진수 0 o
    System.out.println(0X064); //16진수 0 o 
  }
}
